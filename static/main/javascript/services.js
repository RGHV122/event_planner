function social_media_facebook(){
	var facebook_span=document.getElementsByClassName("social-media-facebook")[0];
	var instagram_span=document.getElementsByClassName("social-media-instagram")[0];
	var youtube_span=document.getElementsByClassName("social-media-youtube")[0];
	var whatsapp_span=document.getElementsByClassName("social-media-whatsapp")[0];
	facebook_span.style.display="block";
	instagram_span.style.display="none";
	youtube_span.style.display="none";
	whatsapp_span.style.display="none";

}
